"""
For + Range
range -> range(start, stop, step)
"""

numeros = range(10)

for numero in numeros:
    print(numero)


numeros_2 = range(5, 10)

print('*********************')

for numero_2 in numeros_2:
    print(numero_2)

print('*********************')

numeros_3 = range(0, 10, 2)

for numero_3 in numeros_3:
    print(numero_3)